package Exam02;
import java.util.*;

public class Ex08 {
    public static void main(String[] args) {
        int[] nums1 = new int[4];
        System.out.println(Arrays.toString(nums1)); // [0, 0, 0, 0]

        boolean[] bools = new boolean[4];
        System.out.println(Arrays.toString(bools)); // [false, false, false, false]

        String[] strs = new String[4];
        System.out.println(Arrays.toString(strs)); // [null, null, null, null]
    }
}
